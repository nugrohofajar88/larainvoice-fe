    /**
     * Check if a parent menu should be expanded
     */
    public static function isParentActive(array $children): bool
    {
        foreach ($children as $child) {
            if (self::isActive($child['key'])) {
                return true;
            }
        }
        return false;
    }

    /**
     * Check if user has permission
     */
    public static function hasPermission(string $key, string $action = 'view'): bool
    {
        // SuperAdmin always has permission
        if (AuthHelper::isSuperAdmin()) {
            return true;
        }

        $permissions = session('permissions', []);
        
        // Map frontend action names to API action names
        $actionMap = [
            'view' => 'read',
            'edit' => 'update',
            'create' => 'create',
            'delete' => 'delete',
            'detail' => 'detail'
        ];
        
        $apiAction = $actionMap[$action] ?? $action;

        // The API returns an associative array: $permissions['module_key']['action']
        if (isset($permissions[$key][$apiAction])) {
            return $permissions[$key][$apiAction];
        }

        return false;
    }
